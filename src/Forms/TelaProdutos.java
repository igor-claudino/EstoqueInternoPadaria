    private void tf_pesqKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_pesqKeyReleased
        // TODO add your handling code here:
        pesq = tf_pesq.getText();
        String ant = "";
        try {
            ProdutoDAO dao = new ProdutoDAO();
            if(pesq.equals("")){
                preencherTabela();
            }else{
              minhaList =  dao.ListPesq(pesq, filtro);
              preecherTabelaPesq(minhaList);
              ant = pesq;
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaProdutos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado!!", "Erro", JOptionPane.ERROR_MESSAGE);
            tf_pesq.setText(ant);
        } catch (ParseException ex) {
            Logger.getLogger(TelaProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tf_pesqKeyReleased

