    public void preencherTabela() throws SQLException, ParseException{
            
            ProdutoDAO dao = new ProdutoDAO();
            minhaList = dao.List(filtro);
            MyTableModel mtb = new MyTableModel(Produto.class, minhaList, tb_prod);
            tb_prod.setModel(mtb);
            tb_prod.getColumnModel().getColumn(0).setPreferredWidth(300);
            tb_prod.getColumnModel().getColumn(1).setPreferredWidth(100);
            tb_prod.getColumnModel().getColumn(2).setPreferredWidth(100);
            tb_prod.getColumnModel().getColumn(3).setPreferredWidth(300);
    }
