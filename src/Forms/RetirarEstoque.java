    private void jb_reporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_reporActionPerformed
        // TODO add your handling code here:
        p.setQuant((p.getQuant() - Double.parseDouble(tf_quantR.getText())));
        if(p.getQuant()<0){
            JOptionPane.showMessageDialog(null, "Quantidade no estoque Ã© insuficiente!!");
            tf_quantR.setText("");
            tf_quantR.grabFocus();
        }else{
        try {
            double ret = Double.parseDouble(tf_quantR.getText());
            ProdutoDAO dao = new ProdutoDAO();
            RetiroDAO rdao = new RetiroDAO();
            dao.reporEstoque(p);
            rdao.addRetiro(p, ret);
            new TelaProdutos(user, senha).show();
            dispose();
            
        } catch (SQLException ex) {
            Logger.getLogger(ReporEstoque.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
    }//GEN-LAST:event_jb_reporActionPerformed

